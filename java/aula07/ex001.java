package aula07;

public class ex001 {
    public static void main (String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }

        System.out.print("\n");

        for (int i = 5; i >= 0; i--) {
            System.out.println(i);
        }

        System.out.print("\n");

        for (int i = 0; i <= 10; i += 2) {
            System.out.println(i);
        }
    }
}
